import Foundation

class CXDAppearance {
   class func primaryColor() -> UIColor {
        return UIColor(red: 222/256, green: 171/256, blue: 36/256, alpha: 1)
    }
    
    class func primaryDarkColor() -> UIColor {
        return UIColor(red: 177/256, green: 136/256, blue: 28/256, alpha: 1)
    }
    
    class func primaryBackgroundColor() -> UIColor {
        return UIColor(red: 91/256, green: 91/256, blue: 91/256, alpha: 1)
    }
    
    class func primaryBackgroundDarkColor() -> UIColor {
        return UIColor(red: 51/256, green: 51/256, blue: 51/256, alpha: 1)
    }
}
